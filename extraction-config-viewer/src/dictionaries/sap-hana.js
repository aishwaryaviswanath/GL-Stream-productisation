/**
 * Categorize a SAP HANA table into a functional group.
 * Add new categories or update mappings as needed.
 */
export function categorize(tableName) {
  if (["BKPF", "BSEG", "BSEC", "BSET", "BSAS", "WITH_ITEM"].includes(tableName)) return "Accounting";
  if (["VBRK", "VBRP", "VBPA", "VBAK", "KONV"].includes(tableName)) return "Billing & Sales";
  if (["EKKO", "EKPO", "RBKP", "RSEG"].includes(tableName)) return "Purchasing & Invoicing";
  if (["MKPF", "MSEG", "LIKP", "LIPS"].includes(tableName)) return "Material & Delivery";
  if (["MARC", "MBEW", "MAKT"].includes(tableName)) return "Material Master";
  if (["LFA1", "LFB1", "KNA1", "KNB1", "ADRC", "ADR6"].includes(tableName)) return "Business Partners";
  if (
    tableName.startsWith("T0") || tableName.startsWith("T1") ||
    tableName.startsWith("T5") || tableName.startsWith("T6") ||
    ["TCURR", "TCURX", "TSPAT", "TKUKT", "TVFKT", "SKA1", "SKAT", "T077Z", "T683S", "T685T"].includes(tableName)
  ) return "Config & Reference Data";
  if (["J_1BBRANCH", "J_1IMOVEND", "J_1IMOCUST", "J_1I_SECCO_CIT", "FIWTIN_TAN_EXEM", "T059Z", "T059O", "T059ZT"].includes(tableName)) return "India Localization";
  if (tableName === "CEPC") return "Controlling";
  return "Other";
}

